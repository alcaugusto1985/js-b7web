let idade = 20

if (idade === "20") {
    console.log("É uma string")
} else {
    console.log("É um number")
}

/* Quando usamos em condicionais o valor de igual duas vezes (==), ele apenas verifica se os valores são semlhantes.
Para verificarmos se os valores dão do mesmo tipo, é preciso utilizar o valor de 3 sinais de igual (===), como mostra a condicional acima.
No caso a cima, estamos verificando se a variável idade é um number ou uma string.  */