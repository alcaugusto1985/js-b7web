/* Condicional ternário 
Utilizada para fazer verificações de condicionais simples. 
O valores que venham a seguir da interrogação (?) são TRUE , e os que vierem após o sinal de dois pontos (:) É FALSE. */
let preco = 47
let carne = ((preco <= 45) ? "A carne está barata" : "A carne está cara")

console.log(carne)
console.log("===============================================================")

let idade = 20
let maiorIdade = ((idade >= 18) ? "É MAIOR de idade!" : "É MENOR de idade!")

console.log(maiorIdade)
